  <h1 class="m3">About Us</h1>
  <article class="photo-article">
    <p class="m3 ampstart-dropcap">
      25th Hour Crafts is the brainchild of Kayla and Scott Bitz brought to life by our imaginations and talents.
      It was started in Milwaukee, WI in September of 2015 though we currently live in Oak Creek, WI.
    </p>
    <p class="m3">
      Everything we sell started by learning something new either through a club in college like the Knit and Crochet Club for Kayla
      or YouTube videos that taught chainmaille weaves for Scott. Eventually, we began making things for others besides ourselves for
      Christmas or birthday gifts and not long after, their friends were also asking for personalized handmade crafts similar
      to something we made for our friends and familiy thus formed the idea to sell them to the masses.
    </p>
    <p class="m3">
      Initially, we started vending at craft fairs that were really close to where we lived, however,
      we quickly learned that the nerdy and geeky things that appealed to us and that
      we most often made, did not sell so well with the general public.
      This caused us to second guess our idea, but after seeing a pretty good turnout
      at a Christmas fair, we thought we'd keep it up and see where it went.
    </p>
    <p class="m3">
      This led us vending at a convention we had attended while in college together at MSOE and that's
      <a href="https://www.facebook.com/Concinnitycon/" target="_blank">Concinnity/Kappa Kon</a>.
      We knew the president of the club (MAGE) that ran the event since she was Kayla's sorority sister
      and she told us to try to be vendors there since they were looking to have more alum and student
      vendors. Thankfully we were accepted and absolutely killed it! This told us we needed to go where
      "our people" were at: conventions. Now we travel around the upper midwest
      (hopefully we can travel farther soon as we've been invited out to a con in Las Vegas!)
      appearing in the Artist Alley at conventions like <a href="http://chicagotardis.com/" target="_blank">Chicago Tardis</a>,
      <a href="http://console-room.com/" target="_blank">CONsole Room</a>,
       and <a href="http://wizardworld.com/comiccon/madison" target="_blank">Wizard World</a> in Madison, WI.
    </p>
    <p class="m3 bold italic">
      Below is a short breakdown of what each of the two crafters does:
    </p>
    <h2 class="m3 center h2 italic">Kayla</h2>
    <ul class="m0">
      <li>Dangle Earrings from beads and/or charms</li>
      <li>Charm necklaces</li>
      <li>Knitted scarves of both the Fashion and Nerdy variety like the 4th Doctor&#39;s Scarf</li>
      <li>Crocheted Amigurumi and Plushies</li>
      <li>Licensed Character Fleece single layer blankets with a chrocheted edge</li>
    </ul>
    <p class="m3">
      She also chrochets blankets, shawls, and pillows and knits shawls for friends, family, and volunteer work like <a href="https://www.facebook.com/Threads-of-Compassion-153298621390107/" target="_blank">Threads of Compassion</a>.
    </p>
    <h2 class="m3 center h2 italic">Scott</h2>
    <p class="m3">
      Scott pretty much only does chainmaille like making bags, bracelets, earrings, and dragons.
      He also helps with the earrings and necklaces, but that's mostly just for putting on the earwires and clasps since he knows how to close jump rings.
    </p>
    <h2 class="m3 center h2 italic">Together</h2>
    <p class="m3">
      Since they're a married couple they do <em>some</em> things together like making the licensed character cotton fabric bags
      and setting up their booth together, but that's not really a craft.
      For the sewed bags, Scott handles the cutting, pinning, flipping, and drawstring threading and Kayla does most, if not all, of the sewing.
    </p>
