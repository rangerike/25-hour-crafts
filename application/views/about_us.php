  <h1 class="m3">The (Short) History of 25th Hour Crafts</h1>
  <h2 class="m3 bold">If we had another hour in the day, we would use it to craft.</h3>
  <article class="photo-article">
    <h3 class="m3" style="font-size: 0.95rem;">What's in a name?</h3>
    <p class="m3 ampstart-dropcap">
      A name can be everything for a business because it let's people know what you do, but we also wanted it to be meaningful, so
      we thought long and hard about it when we started in September of 2015, but we couldn't come up with anything
      good on the spot so we started without a name. It wasn't until we were working on something completely different that
      Crafty Hobbit Shop came to mind. After all, it was the first Hobbit movie that got us started as a couple, so we tried
      to start with that with the email address and (as you can still tell) that worked!
    </p>
    <p class="m3">
      It wasn't until we went to open our (now defunct) Etsy shop that we found out it was taken!
      This put us in a bit of a bind since we had that name as our email address! However, it wasn't meant to be,
      so we went back to the drawing board and this is when Kay's best friend thought about something that had a timey wimey Dr. Who theme.
      Since she's is a Whovian and the main crafter, we thought that'd be a good idea and so 25th Hour Crafts was born, because,
      after all, if we had another hour in the day, we would use it to craft!
    </p>
    <h3 class="m3" style="font-size: 0.95rem;">Learning and Experimenting is in our Blood!</h3>
    <p class="m3  ampstart-dropcap">
      Everything we make and then sell started by learning something new. It wasn't just watching YouTube videos or
      looking up instructions online, but was also through various clubs in college like the Knit and Crochet Club.
      Eventually, we began making things for family for Christmas or birthday gifts mostly because we couldn't afford to do much else,
      but we also really enjoyed it. Shortly after that, other friends were asking for personalized handmade crafts similar to something we made for
      our familiy and so this gave us the idea to make them for others and so our little handmade business was born!
    </p>
    <h3 class="m3" style="font-size: 0.95rem;">A Rocky Start</h3>
    <p class="m3  ampstart-dropcap">
      Initially, we put a few of Kay's earrings on Etsy, but a mix of bad pictures, confusing product descriptions, and stiff competition caused them to not sell.
      This got Kay discouraged, but even with that not working out as expected, we were already prepping for our first craft fair. We purchased a few necessities but
      from the first moment we were out there, something didn't feel right. This caused us to second guess our idea, but after seeing a pretty good turnout
      at a Christmas fair, we thought we'd keep it up and see where it went.
    </p>
    <p class="m3">
      This led us vending at a convention we had attended while in college together at MSOE and that's
      <a href="https://www.facebook.com/Concinnitycon/" target="_blank">Concinnity/Kappa Kon</a>.
      We knew the president (at the time) of the "MAGE" club that ran the event since she was Kay's sorority sister
      and she told us to try to be vendors there since they were looking to have more alum and student's selling things there.
      Thankfully we were accepted and absolutely killed it! This told us we needed to go where
      "our people" were at: conventions!
    </p>
    <p class="m3">So far, we've traveled around Wisconsin, Illinois, Minnesota, and Missouri appearing in Artist Alley's or Dealer Rooms at conventions or Geeky Expos
      like <a href="http://chicagotardis.com/" target="_blank">Chicago Tardis</a>,
      <a href="http://www.geekcraftexpo.com/" target="_blank">GeekCraft Expo</a> (Madison, St. Louis, and (hopefully in 2019) Milwaukee),
      <a href="http://garycon.com/" target="_blank">Garycon</a>,
      <a href="https://www.mightyconshows.com/show/milwaukee" target="_blank">Mighty Con Milwaukee</a>, etc. and the rest is history!
    </p>
